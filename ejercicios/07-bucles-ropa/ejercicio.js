// Lista de ropa

let ropa = ["camisa", "pantalon", "zapatos", "sombrero", "chaqueta"];

// Bucle for para imprimir cada prenda de ropa

for (let i = 0; i < ropa.length; i++) {
    console.log(ropa[i]);
}

// Bucle while para imprimir cada prenda de ropa

let j = 0;
while (j < ropa.length) {
    console.log(ropa[j]);
    j++;
}
